const { ethers } = require("hardhat");
const fs = require('fs');
const crypto = require('crypto');
const axios = require('axios');
require('dotenv').config();

async function verifySBTRegistration(tokenIdOrAddress) {
    console.log("üîç Verifying SBT Registration from Registry...\n");

    try {
        // Get network info
        const network = await ethers.provider.getNetwork();
        console.log(`üåê Network: ${network.name} (chainId: ${network.chainId})`);

        // Load SBT contract
        const sbtContractAddress = process.env.SBT_CONTRACT_ADDRESS;
        if (!sbtContractAddress) {
            throw new Error("SBT_CONTRACT_ADDRESS not found in .env file");
        }

        console.log(`üìÑ SBT Contract: ${sbtContractAddress}`);

        // Connect to contract
        const SBT = await ethers.getContractFactory("SBT");
        const sbt = SBT.attach(sbtContractAddress);

        // Verify contract is accessible
        const contractName = await sbt.name();
        const contractSymbol = await sbt.symbol();
        const contractOwner = await sbt.owner();
        const totalSupply = await sbt.totalSupply();

        console.log("\nüìã CONTRACT INFO:");
        console.log("‚ïê".repeat(70));
        console.log(`üè∑Ô∏è Name: ${contractName}`);
        console.log(`üéØ Symbol: ${contractSymbol}`);
        console.log(`üëë Owner: ${contractOwner}`);
        console.log(`üìä Total Supply: ${totalSupply.toString()}`);

        let tokenId, walletAddress;

        // Determine if input is token ID or wallet address
        if (tokenIdOrAddress && tokenIdOrAddress.startsWith('0x') && tokenIdOrAddress.length === 42) {
            // It's a wallet address
            walletAddress = tokenIdOrAddress;
            console.log(`\nüîç Looking up SBT for wallet: ${walletAddress}`);
            
            const tokenIdBN = await sbt.holderToken(walletAddress);
            tokenId = tokenIdBN.toString();
            
            if (tokenId === "0") {
                throw new Error(`No SBT found for wallet address: ${walletAddress}`);
            }
        } else if (tokenIdOrAddress) {
            // It's a token ID
            tokenId = tokenIdOrAddress;
            console.log(`\nüîç Verifying SBT Token ID: ${tokenId}`);
            
            // Get wallet address from token
            walletAddress = await sbt.ownerOf(tokenId);
        } else {
            // No parameter - use from environment or minting file
            const mintingFile = "./data/contracts/sbt-minting.json";
            if (fs.existsSync(mintingFile)) {
                const mintingData = JSON.parse(fs.readFileSync(mintingFile, 'utf8'));
                tokenId = mintingData.tokenId;
                walletAddress = mintingData.owner;
                console.log(`\nüîç Using data from minting file - Token ID: ${tokenId}`);
            } else {
                // Use environment variables
                tokenId = process.env.SBT_TOKEN_ID || "1";
                console.log(`\nüîç Using Token ID from .env: ${tokenId}`);
                walletAddress = await sbt.ownerOf(tokenId);
            }
        }

        console.log(`üÜî Token ID: ${tokenId}`);
        console.log(`üë§ Wallet Address: ${walletAddress}`);

        // Comprehensive SBT verification
        console.log("\nüîç REGISTRY VERIFICATION:");
        console.log("‚ïê".repeat(70));

        // 1. Basic token info
        const tokenOwner = await sbt.ownerOf(tokenId);
        const tokenURI = await sbt.tokenURI(tokenId);
        const tokenDID = await sbt.tokenDID(tokenId);
        const tokenHolder = await sbt.tokenHolder(tokenId);

        console.log(`‚úÖ Token exists: YES`);
        console.log(`üë§ Owner: ${tokenOwner}`);
        console.log(`üë§ Holder mapping: ${tokenHolder}`);
        console.log(`üîó Token URI: ${tokenURI}`);
        console.log(`üÜî DID: ${tokenDID}`);

        // 2. VC Hash verification
        const latestVCHash = await sbt.getLatestVC(tokenId);
        const allVCHashes = await sbt.getAllVCHashes(tokenId);
        const vcHashCount = await sbt.getVCHashCount(tokenId);

        console.log(`\nüîê VC HASH REGISTRY:`);
        console.log(`üìä Total VC Hashes: ${vcHashCount.toString()}`);
        console.log(`üîê Latest VC Hash: ${latestVCHash}`);
        
        if (allVCHashes.length > 1) {
            console.log(`üìú VC Hash History:`);
            allVCHashes.forEach((hash, index) => {
                console.log(`  ${index + 1}. ${hash}`);
            });
        }

        // 3. Cross-reference with original VC data
        console.log(`\nüîç CROSS-REFERENCE VERIFICATION:`);
        
        // Load original signed VC for comparison
        const signedVCFile = "./data/vc-system/verifiable-credential-signed.json";
        let originalVCHash = null;
        let vcDataMatch = false;

        if (fs.existsSync(signedVCFile)) {
            const signedVC = JSON.parse(fs.readFileSync(signedVCFile, 'utf8'));
            const vcString = JSON.stringify(signedVC);
            const calculatedHash = crypto.createHash('sha256').update(vcString).digest('hex');
            originalVCHash = `0x${calculatedHash}`;
            
            vcDataMatch = originalVCHash === latestVCHash;
            
            console.log(`üîê Original VC Hash: ${originalVCHash}`);
            console.log(`üîê Registry VC Hash: ${latestVCHash}`);
            console.log(`‚úÖ VC Hash Match: ${vcDataMatch ? 'YES' : 'NO'}`);
            
            // Additional VC details
            console.log(`\nüìã VC CREDENTIAL DETAILS:`);
            console.log(`üë§ Subject Name: ${signedVC.credentialSubject.full_name || 'N/A'}`);
            console.log(`üìß Subject Email: ${signedVC.credentialSubject.email || 'N/A'}`);
            console.log(`‚úÖ KYC Status: ${signedVC.credentialSubject.kycStatus || 'N/A'}`);
            console.log(`üí∞ Investment Limit: ${signedVC.credentialSubject.investment_limit || 'N/A'}`);
            console.log(`üè¶ Risk Profile: ${signedVC.credentialSubject.risk_profile || 'N/A'}`);
            
            // Wallet verification
            const vcWalletETH = signedVC.credentialSubject.wallets?.eth;
            const walletMatch = vcWalletETH === walletAddress;
            console.log(`\nüí≥ WALLET VERIFICATION:`);
            console.log(`üí≥ VC Wallet (ETH): ${vcWalletETH || 'N/A'}`);
            console.log(`üí≥ SBT Owner: ${walletAddress}`);
            console.log(`‚úÖ Wallet Match: ${walletMatch ? 'YES' : 'NO'}`);
        } else {
            console.log(`‚ö†Ô∏è Original VC file not found: ${signedVCFile}`);
        }

        // 4. IPFS metadata verification
        console.log(`\nüåê IPFS METADATA VERIFICATION:`);
        if (tokenURI.startsWith('ipfs://')) {
            const ipfsHash = tokenURI.replace('ipfs://', '');
            console.log(`üîó IPFS Hash: ${ipfsHash}`);
            
            try {
                // Try to fetch IPFS content
                const ipfsGateways = [
                    `https://ipfs.io/ipfs/${ipfsHash}`,
                    `https://gateway.pinata.cloud/ipfs/${ipfsHash}`,
                    `https://cloudflare-ipfs.com/ipfs/${ipfsHash}`
                ];

                let ipfsContent = null;
                for (const gateway of ipfsGateways) {
                    try {
                        console.log(`üîç Trying gateway: ${gateway}`);
                        const response = await axios.get(gateway, { timeout: 10000 });
                        ipfsContent = response.data;
                        console.log(`‚úÖ IPFS Content Retrieved: YES`);
                        break;
                    } catch (gatewayError) {
                        console.log(`‚ùå Gateway failed: ${gateway.split('/')[2]}`);
                    }
                }

                if (ipfsContent) {
                    console.log(`üìÑ IPFS Content Type: ${typeof ipfsContent}`);
                    if (typeof ipfsContent === 'object') {
                        console.log(`üìã Content Keys: ${Object.keys(ipfsContent).join(', ')}`);
                        
                        // Check if it matches our VC structure
                        if (ipfsContent.credentialSubject) {
                            console.log(`‚úÖ Contains VC Structure: YES`);
                            console.log(`üë§ IPFS Subject: ${ipfsContent.credentialSubject.full_name || 'N/A'}`);
                        }
                    }
                }
            } catch (ipfsError) {
                console.log(`‚ùå IPFS Retrieval Failed: ${ipfsError.message}`);
            }
        } else {
            console.log(`‚ö†Ô∏è Token URI is not IPFS format: ${tokenURI}`);
        }

        // 5. Soulbound verification (try transfer)
        console.log(`\nüîí SOULBOUND VERIFICATION:`);
        try {
            // This should fail for soulbound tokens
            const transferData = sbt.interface.encodeFunctionData(
                "transferFrom", 
                [walletAddress, walletAddress, tokenId]
            );
            
            // Estimate gas (should fail)
            await ethers.provider.estimateGas({
                to: sbtContractAddress,
                data: transferData
            });
            
            console.log(`‚ùå Soulbound Check: FAILED - Token appears transferable!`);
        } catch (transferError) {
            if (transferError.message.includes("soulbound") || 
                transferError.message.includes("non-transferable")) {
                console.log(`‚úÖ Soulbound Check: PASSED - Token is non-transferable`);
            } else {
                console.log(`‚ö†Ô∏è Soulbound Check: Unclear - ${transferError.message}`);
            }
        }

        // 6. Registry lookup by address
        console.log(`\nüîç REGISTRY LOOKUP VERIFICATION:`);
        const sbtInfo = await sbt.getSBTInfo(walletAddress);
        const [regTokenId, regDID, regVCHash] = sbtInfo;
        
        console.log(`üÜî Registry Token ID: ${regTokenId.toString()}`);
        console.log(`üÜî Registry DID: ${regDID}`);
        console.log(`üîê Registry VC Hash: ${regVCHash}`);
        
        const registryConsistent = (
            regTokenId.toString() === tokenId &&
            regDID === tokenDID &&
            regVCHash === latestVCHash
        );
        
        console.log(`‚úÖ Registry Consistency: ${registryConsistent ? 'PASSED' : 'FAILED'}`);

        // 7. Contract events verification
        console.log(`\nüìú EVENT LOG VERIFICATION:`);
        try {
            // Get SBTMinted events for this token
            const mintedEvents = await sbt.queryFilter(
                sbt.filters.SBTMinted(tokenId),
                0,
                'latest'
            );

            console.log(`üìä SBTMinted Events: ${mintedEvents.length}`);
            if (mintedEvents.length > 0) {
                const mintEvent = mintedEvents[0];
                console.log(`üì¶ Mint Block: ${mintEvent.blockNumber}`);
                console.log(`üìã Mint Tx: ${mintEvent.transactionHash}`);
            }

            // Get VCHashRegistered events
            const vcEvents = await sbt.queryFilter(
                sbt.filters.VCHashRegistered(tokenId),
                0,
                'latest'
            );

            console.log(`üìä VCHashRegistered Events: ${vcEvents.length}`);
        } catch (eventError) {
            console.log(`‚ö†Ô∏è Event query failed: ${eventError.message}`);
        }

        // Summary
        console.log(`\nüéâ VERIFICATION SUMMARY:`);
        console.log("‚ïê".repeat(70));
        console.log(`üÜî Token ID: ${tokenId}`);
        console.log(`üë§ Owner: ${walletAddress}`);
        console.log(`‚úÖ Registry Status: VERIFIED`);
        console.log(`üîê VC Hash Status: ${vcDataMatch ? 'VERIFIED' : 'UNVERIFIED'}`);
        console.log(`üîí Soulbound Status: VERIFIED`);
        console.log(`üåê IPFS Status: ${tokenURI.startsWith('ipfs://') ? 'IPFS' : 'OTHER'}`);

        // Etherscan links
        console.log(`\nüîó BLOCKCHAIN LINKS:`);
        console.log("‚ïê".repeat(70));
        if (network.name === 'sepolia') {
            console.log(`üåê Contract: https://sepolia.etherscan.io/address/${sbtContractAddress}`);
            console.log(`üéñÔ∏è Token: https://sepolia.etherscan.io/token/${sbtContractAddress}?a=${tokenId}`);
            console.log(`üë§ Owner: https://sepolia.etherscan.io/address/${walletAddress}`);
        }

        return {
            verified: true,
            tokenId: tokenId,
            owner: walletAddress,
            did: tokenDID,
            vcHash: latestVCHash,
            vcHashMatch: vcDataMatch,
            ipfsURI: tokenURI,
            registryConsistent: registryConsistent
        };

    } catch (error) {
        console.error("\n‚ùå VERIFICATION FAILED:");
        console.error("‚ïê".repeat(70));
        console.error("Error:", error.message);
        
        if (error.message.includes("No SBT found")) {
            console.error("üîç Solution: Check that the wallet address has an SBT");
        } else if (error.message.includes("token does not exist")) {
            console.error("üÜî Solution: Check that the token ID is valid");
        } else if (error.message.includes("SBT_CONTRACT_ADDRESS")) {
            console.error("üîß Solution: Update .env with correct SBT contract address");
        }
        
        process.exit(1);
    }
}

// Helper function to verify multiple tokens
async function verifyAllSBTs() {
    console.log("üîç Verifying All SBTs in Registry...\n");
    
    try {
        const sbtContractAddress = process.env.SBT_CONTRACT_ADDRESS;
        const SBT = await ethers.getContractFactory("SBT");
        const sbt = SBT.attach(sbtContractAddress);
        
        const totalSupply = await sbt.totalSupply();
        console.log(`üìä Total SBTs: ${totalSupply.toString()}`);
        
        for (let i = 1; i <= totalSupply.toNumber(); i++) {
            console.log(`\nüîç Verifying Token ID: ${i}`);
            await verifySBTRegistration(i.toString());
        }
        
    } catch (error) {
        console.error("‚ùå Bulk verification failed:", error.message);
    }
}

// Command line handling
async function main() {
    const args = process.argv.slice(2);
    
    if (args.includes('--all')) {
        await verifyAllSBTs();
    } else {
        const tokenIdOrAddress = args[0];
        await verifySBTRegistration(tokenIdOrAddress);
    }
    
    console.log("\n‚úÖ VERIFICATION COMPLETED!");
}

// Run if called directly
if (require.main === module) {
    main()
        .then(() => process.exit(0))
        .catch((error) => {
            console.error("‚ùå Error:", error);
            process.exit(1);
        });
}

module.exports = { verifySBTRegistration, verifyAllSBTs };